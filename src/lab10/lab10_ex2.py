# Ввод последовательности символов
sequence = input("Введите последовательность символов: ")

# Находим множество цифр, встречающихся в последовательности
digits_set = {char for char in sequence if char.isdigit()}

# Вывод множества цифр
print("Множество цифр, встречающихся в последовательности:", digits_set)