# Ввод трех чисел
a = float(input("Введите число a: "))
b = float(input("Введите число b: "))
c = float(input("Введите число c: "))

# Подсчет количества положительных чисел
count = 0
if a > 0:
    count += 1
if b > 0:
    count += 1
if c > 0:
    count += 1

# Вывод результата
print("Количество положительных чисел:", count)