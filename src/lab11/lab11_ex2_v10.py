# Ввод списка оценок на первом экзамене
grades = [int(input(f"Оценка абитуриента {i+1}: ")) for i in range(25)]

# Подсчет количества абитуриентов, не допущенных ко второму экзамену
rejected_count = sum(1 for grade in grades if grade == 2)

# Вывод результата
print("Число абитуриентов, не допущенных ко второму экзамену:", rejected_count)